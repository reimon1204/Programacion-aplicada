#Operador para obteener el residuo de una division
#29 entre 5, da un valor de 5 con un resido de 4, por lo que el valor arrojado es 4(el residuo de dicha division)
print(29%5)
# Imprime 2 porque 32 / 3 es 10 con un residuo de 2
print(32%3)
#Si la division es exacta y no tiene residuo, el valor arrojado sera 0
print(44%2)